    <h4>I. Wstęp</h4>
    <p>
        Celem niniejszego Regulaminu jest określenie zasad i warunków świadczenia usług za
        pośrednictwem Serwisu dostępnego przez aplikację - Fuzja na rzecz
        klientów zgodnie z ustawą z dnia 18 lipca 2002 r. o świadczeniu usług drogą elektroniczną.
    </p>
    <h4>II. Definicje</h4>

    <p>W ramach niniejszej Regulaminu wykorzystano następujące definicje:</p>
    <p>
        1. Administrator / Fuzja / Sprzedawca - oznacza Fuzja, prowadzony przez
        Tomasz Niedzielski zam. Bartodzieje 1, 26-631 Jastrzębia.
    </p>
    <p>
        2. Regulamin - oznacza regulamin świadczenia usług drogą elektroniczną w ramach aplikacji
        udostępnionej pod adresem: https://play.google.com/store/apps/details?id=com.fuzja.fuzja.
    </p>
    <p>
        3. Użytkownik, Użytkownicy - oznaczają każdą osobę, która korzysta z usług oferowanych
        przez Aplikację.
    </p>
    <p>
        4. Klient – oznacza Użytkownika, który dokonał zakupu u Sprzedawcy.
    </p>
    <p>
        5. Przedsiębiorca - osoba fizyczna, osoba prawna i jednostka organizacyjna niebędąca osobą
        prawną, której odrębna ustawa przyznaje zdolność prawną, wykonująca we własnym
        imieniu działalność gospodarczą, która korzysta ze Sklepu.
    </p>
    <p>
        6. Umowa zawarta na odległość – umowa zawarta z Klientem w ramach zorganizowanego
        systemu zawierania umów na odległość (w ramach Sklepu), bez jednoczesnej fizycznej
        obecności stron, z wyłącznym wykorzystaniem jednego lub większej liczby środków
        porozumiewania się na odległość do chwili zawarcia umowy włącznie.
    </p>
    <p>
        7. Produkt – oznacza dostępną u Sprzedawcy usługę lub rzecz ruchomą będącą przedmiotem
        Umowy Sprzedaży między Klientem a Sprzedawcą.
    </p>
    <p>
        8. Umowa sprzedaży – umowa sprzedaży Produktu zawierana albo zawarta między Klientem
        a Sprzedawcą za pośrednictwem Sklepu internetowego. Przez Umowę Sprzedaży rozumie
        się też – stosowanie do cech Produktu – umowę o świadczenie usług i umowę o dzieło.
    </p>
    <p>
        9. Formularz zamówienia – formularz umożliwiający wprowadzenie zamawianego produktu
        oraz formy płatności.
    </p>
    <p>
        10. Zamówienie – oświadczenie Klienta składane za pomocą Formularza zamówienia,
        zmierzające do zawarcia Umowy sprzedaży Produktu ze Sprzedawcą.
    </p>

    <h4>III. Informacje ogólne</h4>
    <p>
        1. Przedmiotem usług świadczonych przez aplikację jest korzystanie z jej funkcji.
    </p>
    <p>
        2. Aplikacja Fuzja działa na zasadach określonych w niniejszym Regulaminie. Każdy Użytkownik
        korzystający z niej zobowiązany jest do zapoznania i poszanowania niniejszego
        Regulaminu.
    </p>
    <p>
        3. Aplikacja szanuje prawo Użytkowników do prywatności. Zasady te zostały spisane w osobnym
        dokumencie zwanym Polityką prywatności.
    </p>
    <p>
        4. Jeżeli Regulamin nie mówi inaczej, zakazane jest pobieranie lub wtórne wykorzystywanie w
        jakimkolwiek celu istotnej części bazy danych zawartej w Aplikacji w szczególności danych
        podanych przez innych Użytkowników niż obecny.
    </p>
    <p>
        5. Niedozwolone jest udostępnianie konta Użytkownika osobom trzecim.
    </p>
    <p>
        6. W celu zachowania prawidłowego funkcjonowania Aplikacji Użytkownik zobowiązany jest do
        wprowadzania danych zgodnych z rzeczywistością.
    </p>

    <h4>IV. Założenie konta użytkownika</h4>
    <p>
        1. W celu założenia konta Użytkownik wypełnia formularz rejestracji konta. Niezbędne jest
        podanie adresu e-mail, hasła oraz zaakceptowanie Regulaminu Serwisu.
    <p>
    </p>
        2. Założenie konta jest darmowe.
    <p>
    </p>
        3. Logowanie odbywa się poprzez podanie adresu e-mail oraz hasła.
    <p>
    </p>
        4. Aby usunąć konto użytkownik zobowiązany jest wysłać takie żądanie do Administratora.
    <p>
    <h4>V. Zakup usługi</h4>
    <p>
        1. Zakup usługi odbywa się poprzez wypełnienie Formularza zamówienia Produktu przez
        Klienta.
    </p>
    <p>
        2. Po złożeniu Zamówienia na Produkt Sprzedawca potwierdza jego otrzymanie i przyjmuje
        zamówienie do realizacji.
    </p>
    <p>
        3. Produkt jest wysyłany natychmiast po zaksięgowaniu pieniędzy na koncie Sprzedawcy, z
        zastrzeżeniem możliwości przedłużenia tego okresu o 24h.
    </p>
    <p>
        4. Dostawa produktu jest bezpłatna.
    </p>

    <h4>VI. Reklamacja</h4>
    <p>
        1. Reklamację należy zgłosić pisemnie lub drogą elektroniczną na podany w Regulaminie adres
        Administratora.
    </p>
    <p>
        2. Od wysłania reklamacji Administrator ma 14 dni na ustosunkowanie się , jeśli nie udzielił
        odpowiedzi uznaje się, że żądanie za zasadne.
    </p>
    <p>
        3. W przypadku wystąpienia wady zakupionego u Sprzedawcy towaru Klient ma prawo do
        reklamacji w oparciu o przepisy dotyczące rękojmi w kodeksie cywilnym. Jeżeli Klientem
        jest Przedsiębiorcą, strony wyłączają odpowiedzialność z tytułu rękojmi.
    </p>
    <p>
        4. Jako wadę fizyczną rozumie się niezgodność właściwości Produktu z umową.
    </p>
    <p>
        5. W sytuacji wystąpienia wady Klient ma prawo złożyć reklamację z tytułu rękojmi i zażądać
        jednego z czterech działań:
    </p>
    <p>a. ponownego, nieodpłatnego udostępnienia Produktu,</p>
    <p>b. usunięcia wad,</p>
    <p>c. obniżenia ceny,</p>
    <p>d. odstąpienia od umowy – o ile wada jest istotna. Wybór żądania należy do Klienta</p>
    <p>
        6. W przypadku, kiedy Klient żąda ponownego nieodpłatnego udostępnienia Produktu, Sklep
        może odmówić spełnienia tego żądania w przypadku, kiedy opcja wybrana przez Klienta:
    </p>
    <p>a. byłaby niemożliwa do zrealizowania dla Sklepu</p>
    <p>b. w porównaniu z drugim z możliwych żądań wymagałaby nadmiernych kosztów.</p>
    <p>
        7. W przypadku rozpatrzenia reklamacji na korzyść Klienta – Sklep, w zależności od
        przedstawionego żądania, umożliwi ponowne nieodpłatne udostępnienie Produktu, usunie
        wady lub zwróci należność za reklamowany Produkt w ciągu siedmiu dni od daty
        rozpatrzenia reklamacji.
    </p>
    <p>
        8. Reklamacji nie podlega styl graficzny, jak również forma wykonania Produktów, które są
        wynikiem autorskiego projektu Sprzedawcy.
    </p>

    <h4>VII. Bezpieczeństwo i odpowiedzialność</h4>
    <p>
        1. Dla zachowania bezpieczeństwa Użytkownika, Aplikacja zaleca korzystanie z aktualnego
        oprogramowania, w tym oprogramowania antywirusowego.
    </p>
    <p>
        2. Zakazane jest tworzenie treści powszechnie uznawanych za
        obraźliwe, zawierające wulgaryzmy, a także zawierające treści o charakterze
        pornograficznym. A także przesyłanie treści mogące wpłynąć na bezpieczeństwo systemu
        takie jak wirusy.
    </p>

    <h4>VIII. Wymagania techniczne</h4>
    <p>
        1. Użytkownik zobowiązany jest do posiadania:
    </p>
    <p>a. Dostępu do internetu,</p>
    <p>b. Systemu operacyjnego Android,</p>
    <p>c. Zezwalać aplikacji na korzystanie z obsługi technologii Javascript,</p>
    <p>d. Posiadać konto poczty elektronicznej.</p>
    <p>
        2. Aplikacja Fuzja zastrzega, że korzystanie z Serwisu może być obarczone konsekwencjami w
        standardowym wymiarze i zaleca Użytkownikom prowadzenie działań ograniczających
        ryzyko.
    </p>

    <h4>IX. Postanowienia końcowe</h4>
    <p>
        1. Aplikacja Fuzja zastrzega sobie prawo do dokonywania zmian w niniejszym regulaminie w
        przypadku wystąpienia sytuacji tego wymagających (wpływających na działanie
        użytkowników) takich jak zmiany obowiązującego prawa, aktualizacja działania serwisu. W
        przypadku zmian wprowadzonych do regulaminu zaczynają one natychmiast obowiązywać
        w stosunku do nowych Użytkowników i z opóźnieniem 2 tygodni dla już istniejących
        Użytkowników po opublikowaniu tych informacji na stronie.
    </p>
    <p>
        2. Aplikacja zastrzega sobie prawo do umieszczania na stronie treści reklamowych.
    </p>
    <p>
        3. Wobec wszystkich spraw dotyczących korzystania z Aplikacji zastosowanie mają
        odpowiednie przepisy prawa polskiego.
    </p>
    <p>
        4. Jeżeli orzeczenie sądowe określi wybrane postanowienie obecne w niniejszym Regulaminie
        za nieważne, to pozostałe wciąż są obowiązujące.
    </p>
    <p>
        5. W sprawach nieuregulowanych w niniejszym Regulaminie mają zastosowanie powszechnie
        obowiązujące przepisy prawa polskiego.
    </p>
    <p>
        6. Pytania i uwagi można kierować na adres fuzja.app@gmail.com.
